function easeCubic(t) {
  return Math.max(0, Math.min(1.0, t))**3;
}

function easeOutBounce(t) {
    if ( t < ( 1 / 2.75 ) ) { return 7.5625 * t * t; }
    else if ( t < ( 2 / 2.75 ) ) { return 7.5625 * ( t -= ( 1.5 / 2.75 ) ) * t + 0.75; }
    else if ( t < ( 2.5 / 2.75 ) ) { return 7.5625 * ( t -= ( 2.25 / 2.75 ) ) * t + 0.9375; }
    else {return 7.5625 * ( t -= ( 2.625 / 2.75 ) ) * t + 0.984375; }
};

function scrollToTop(duration = 1000) {
  const el = document.scrollingElement;
  const initialScroll = el.scrollTop;
  let start = null;

  function step(timestamp) {
    if (!start) {
      start = timestamp;
    }

    const progress = timestamp - start;

    const delta = Math.min(1, progress / duration);

    const newScrollTop = initialScroll - easeOutBounce(delta) * initialScroll;

    el.scrollTop = newScrollTop;

    if (progress < duration) {
      window.requestAnimationFrame(step);
    }
  }

  window.requestAnimationFrame(step);
}

